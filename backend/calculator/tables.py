# ---------------------------------------------------------------------------
# Helper: look up a value from any EPS table
# ---------------------------------------------------------------------------

def lookup_eps(table: list[tuple], eps: float) -> tuple | None:
    """Return the first table row whose EPS range contains the given eps value.
    Returns None if eps is out of range."""
    for row in table:
        if row[0] <= eps <= row[1]:
            return row
    # If eps exceeds the highest row (rare), return the last row
    if eps > table[-1][1]:
        return table[-1]
    return None

